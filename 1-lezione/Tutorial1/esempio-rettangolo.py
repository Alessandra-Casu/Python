# Prendiamo in input la base e l'altezza di un rettangolo utilizzando le funzioni int() ed input()
# dopo eseguiamo il prodotto tra la base e l'altezza e lo memorizziamo nella variabile area.
# infine stampiamo in output il risultato ottenuto

base = int(input("Inserisci la base del tuo rettangolo: "))
altezza = int(input("Inserisci l'altezza del tuo rettangolo: "))

area = base * altezza

print("L'area del tuo rettangolo Ã¨: ", area)
